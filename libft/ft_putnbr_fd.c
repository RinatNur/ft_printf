#include "libft.h"

void		ft_putnbr_fd(unsigned long n)
{
	if (n == INT_MIN)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(n * (-1));
	}
	else
	{
		if (n > 9)
			ft_putnbr(n / 10);
//		ft_putchar_fd((n % 10 + '0'));
	}
}
